import fs from "fs";
import path from "path";

function escapeRegExp(string) {
  return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function replaceInFile(file, replacements) {
  if (!fs.existsSync(file)) {
    console.warn(`⚠️ 文件不存在：${file}`);
    return;
  }

  let content = fs.readFileSync(file, "utf8");
  let modified = false;

  for (const [from, to] of Object.entries(replacements)) {
    const regex = new RegExp(escapeRegExp(from), "g");
    if (regex.test(content)) {
      content = content.replace(regex, to);
      console.log(`📝 ${path.basename(file)}: "${from}" → "${to}"`);
      modified = true;
    }
  }

  if (modified) {
    fs.writeFileSync(file, content);
    console.log(`✅ 更新文件: ${file}\n`);
  } else {
    console.log(`ℹ️ 未修改: ${file}\n`);
  }
}

function replaceInFolder(folderPath, replacements) {
  const files = fs.readdirSync(folderPath);

  for (const file of files) {
    const fullPath = path.join(folderPath, file);
    const stat = fs.statSync(fullPath);

    if (stat.isDirectory()) {
      replaceInFolder(fullPath, replacements);
    } else {
      replaceInFile(fullPath, replacements);
    }
  }
}

